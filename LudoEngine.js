// گوٹی کو آگے بڑھانے کا مین فنکشن
export const getNextPosition = (color, currentPosition, diceRoll) => {
  // اگر گوٹی ابھی گھر کے اندر ہے (Position = -1) اور 6 آیا ہے، تو وہ باہر نکلے گی (Position = 0)
  if (currentPosition === -1) {
    if (diceRoll === 6) return 0; 
    return -1; // اگر 6 نہیں آیا تو گھر میں ہی رہے گی
  }

  const newPosition = currentPosition + diceRoll;

  // اگر گوٹی گیم ختم کر کے ہوم ٹرائینگل میں پہنچ جائے (لڈو میں کل 57 خانے ہوتے ہیں)
  if (newPosition > 57) {
    return currentPosition; // اگر نمبر زیادہ ہے تو گوٹی وہیں رک جائے گی (جیسے جیت کے لیے 2 چاہیے اور 4 آ جائے)
  }

  return newPosition;
};
